    private void hapusTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusTextActionPerformed
        // TODO add your handling code here:
        String nim = hapusNIM.getText();
        String hapus = hapusNIM.getText();
         Iterator<Mahasiswa> iterator = daftarMahasiswa.iterator();
        while (iterator.hasNext()) {
            Mahasiswa data = iterator.next();
            if (data.getNim().equals(hapus)) {
                iterator.remove();
                break;
            }
        }
            try {
                Statement stmt = koneksi.connection.createStatement();
                String sql = "DELETE FROM msiswa WHERE (NIM = '"+nim+"' )";
                stmt.executeUpdate(sql);
                System.out.println("Data berhasil di hapus");
                setTable();
            } catch (SQLException ex) {
                System.out.println(ex);
            }
            
            
    }//GEN-LAST:event_hapusTextActionPerformed

