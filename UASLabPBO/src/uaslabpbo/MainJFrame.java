    private void tambahTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahTextActionPerformed
        // TODO add your handling code here:
        String nim = nimText.getText();
        String nama = namaText.getText();
        String jeniskelamin="";
        if(lakilakiBtn.isSelected()){
            jeniskelamin = "Laki-laki";
        }
        else if(perempuanBtn.isSelected()){
            jeniskelamin = "Perempuan";
        }
        String nohp = hpText.getText();
        String agama = agamaCBX.getSelectedItem().toString();        
        String status="";
        if (statusCheck.isSelected()){
            status="Aktif";
        }else{
            status="Tidak Aktif";
        }
        
        
        Mahasiswa mahasiswa = new Mahasiswa();
        mahasiswa.nim = nim;
        mahasiswa.nama = nama;
        mahasiswa.jeniskelamin =jeniskelamin;
        mahasiswa.nohp = nohp;
        mahasiswa.agama = agama;
        mahasiswa.status = status;
        
        try {
            Statement stmt = koneksi.connection.createStatement();
            String sql = "INSERT INTO msiswa values ('"+mahasiswa.nama+"','"+mahasiswa.nim+"','"
                    +mahasiswa.jeniskelamin+"','"+mahasiswa.nohp+"','"+mahasiswa.agama+"','"
                    +mahasiswa.status+"')";
            stmt.executeUpdate(sql);
            System.out.println("Data berhasil di Input");
            clearData();
        } catch (SQLException ex) {
            System.out.println(ex);
        }
        daftarMahasiswa.add(mahasiswa);
        setTable();
        
        
    }//GEN-LAST:event_tambahTextActionPerformed

