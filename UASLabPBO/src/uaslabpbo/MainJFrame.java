    private void setTable(){
        //mengisi table
        DefaultTableModel model = (DefaultTableModel) mahasiswaTable.getModel();
        model.setRowCount(100); 
        daftarMahasiswa = Mahasiswa.daftarMahasiswa;
        Mahasiswa tempMahasiswa;
        for (int i = 0; i < daftarMahasiswa.size(); i++)
        {
            tempMahasiswa = daftarMahasiswa.get(i);
        
            daftarModel =  mahasiswaTable.getModel();
            daftarModel.setValueAt(tempMahasiswa.nim, jumlahMahasiswa, 0);
            daftarModel.setValueAt(tempMahasiswa.nama, jumlahMahasiswa, 1);
            daftarModel.setValueAt(tempMahasiswa.jeniskelamin, jumlahMahasiswa, 2);
            daftarModel.setValueAt(tempMahasiswa.nohp, jumlahMahasiswa, 3);
            daftarModel.setValueAt(tempMahasiswa.agama, jumlahMahasiswa, 4);
            daftarModel.setValueAt(tempMahasiswa.status, jumlahMahasiswa, 5);
            jumlahMahasiswa++;
        } 
        jumlahMahasiswa = 0;
    }
